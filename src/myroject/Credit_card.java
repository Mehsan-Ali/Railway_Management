    private void NameoncardKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NameoncardKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        
        if(Character.isLetter(c) || Character.isWhitespace(c) || Character.isISOControl(c)){
            Nameoncard.setEditable(true);
        }else{
            Nameoncard.setEditable(false);
        }
    }//GEN-LAST:event_NameoncardKeyTyped

