    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Fill all info :
        
        if(Pin.getText().trim().isEmpty() || Phoneno.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please Fill All Information");
        }
        
        else{
        JOptionPane.showMessageDialog(rootPane, "Payment Received Successfully");
        Ticket tk = new Ticket();
        
        //Transfer data into Ticket
        
        tk.Tamnt.setText(Tkp.getText());
        
        for (int column = 0; column < ejTable.getColumnCount(); column++){
            
            Object value = ejTable.getValueAt(0, column);
            
            switch(ejTable.getColumnName(column)){
                        case "Train Name":
                            tk.Ttname.setText(value.toString());
                            break;
                        case "To":
                            tk.Tto.setText(value.toString());
                            break;
                        case "From":
                            tk.Tfrom.setText(value.toString());
                            break;
                        case "Train no":
                            tk.Tno.setText(value.toString());
                            break;
                        case "Price / Seat":
                            tk.Tpptk.setText(value.toString());
                            break;
                        case "Date":
                            tk.Tdate.setText(value.toString());
                            break;
                        case "Departure":
                            tk.Ttime.setText(value.toString());
                            break;
                        case "Name":
                            tk.Tname.setText(value.toString());
                            break;
                        case "No of Ticket":
                            tk.Tnoft.setText(value.toString());
                            break;
                        case "Nic no":
                            tk.Tnic.setText(value.toString());
                            break;
                        case "Gender":
                            tk.Tgender.setText(value.toString());
                            break;
                        }
        }       
        
        this.dispose();
        tk.setVisible(true);
        tk.toFront();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

