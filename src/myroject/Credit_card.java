    private void PaynActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PaynActionPerformed
        // TODO add your handling code here:
        
        if(Nameoncard.getText().trim().isEmpty() || Cardno.getText().trim().isEmpty() 
           || Edate.getText().trim().isEmpty() || Cvv.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please Fill Information Correctly!");
        }
        
        else{
        
        JOptionPane.showMessageDialog(rootPane, "Payment Received Successfully");
        Ticket tk = new Ticket();
        //// Assigning Values to the text field in Ticket Frame
        
        tk.Tamnt.setText(Total.getText());
        
        for (int column = 0; column < cjTable.getColumnCount(); column++){
            
            Object value = cjTable.getValueAt(0, column);
            
            switch(cjTable.getColumnName(column)){
                        case "Train Name":
                            tk.Ttname.setText(value.toString());
                            break;
                        case "To":
                            tk.Tto.setText(value.toString());
                            break;
                        case "From":
                            tk.Tfrom.setText(value.toString());
                            break;
                        case "Train no":
                            tk.Tno.setText(value.toString());
                            break;
                        case "Price / Seat":
                            tk.Tpptk.setText(value.toString());
                            break;
                        case "Date":
                            tk.Tdate.setText(value.toString());
                            break;
                        case "Departure":
                            tk.Ttime.setText(value.toString());
                            break;
                        case "Name":
                            tk.Tname.setText(value.toString());
                            break;
                        case "No of Ticket":
                            tk.Tnoft.setText(value.toString());
                            break;
                        case "Nic no":
                            tk.Tnic.setText(value.toString());
                            break;
                        case "Gender":
                            tk.Tgender.setText(value.toString());
                            break;
                        }
        }       
        
        tk.setVisible(true);
        tk.toFront();
        this.dispose();
        }
    }//GEN-LAST:event_PaynActionPerformed

