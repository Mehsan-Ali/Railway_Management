    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         // TODO add your handling code here:
    
        if(Name.getText().trim().isEmpty() || Nic.getText().trim().isEmpty() 
           || Notk.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please Fill All Information");
        }
         
            else{
        
        String nme = Name.getText();
        String nic = Nic.getText();
        String nof = Notk.getText();

        
        TableModel model = jTable.getModel();
        
        int rowCount = jTable.getRowCount();
        Reservation rv = new Reservation();
        
        DefaultTableModel model3 = (DefaultTableModel)rv.rjTable.getModel();
        
        for(int i=0 ; i<rowCount ; i++){
            Object[] rowData= new Object[jTable.getColumnCount()];
            for(int j = 0 ; j < jTable.getColumnCount() ; j++){
                rowData[j] = model.getValueAt(i, j);
            }
            model3.addRow(rowData);
        }
        
        int lastRowIndex = model.getRowCount() -1;
        
        model3.setValueAt(nme, lastRowIndex, 7);
        model3.setValueAt(nof, lastRowIndex, 8);
        model3.setValueAt(nic, lastRowIndex, 9);
        if(Male.isSelected()){
            model3.setValueAt(Male.getText(), lastRowIndex, 10);
        }else{
            model3.setValueAt(Female.getText(), lastRowIndex, 10);
        }
        
        
        ////////Assigning Values to the next frame
        
        int a = Integer.parseInt(Notk.getText());
        int b = Integer.parseInt(Price.getText());
        
        double sum=a*b;
        
        rv.Notk.setText(Notk.getText());
        rv.Ptkp.setText(Price.getText());
        
        rv.Top.setText(String.valueOf(sum));
        
//        //// Transfer data into Rservation frame
        
        ////-----------------------------////////////
        this.dispose();
        rv.setVisible(true);
        rv.toFront();
       }
    }//GEN-LAST:event_jButton2ActionPerformed

